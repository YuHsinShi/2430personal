static bool gsl_i2c_read(uint8_t* cmd, uint8_t *buf, uint32_t len)
{
	int i2cret;
	ITPI2cInfo *gsl_evt;

	gsl_evt = alloca(sizeof(*gsl_evt));
	gsl_evt->slaveAddress   = TP_I2C_DEVICE_ID;
	gsl_evt->cmdBuffer      = cmd;
	gsl_evt->cmdBufferSize  = 2;
	gsl_evt->dataBuffer     = buf;
	gsl_evt->dataBufferSize = len;	
	i2cret = read(gTpInfo.tpDevFd, gsl_evt, 1);	
	return i2cret;
}

static bool gsl_i2c_write(uint8_t* cmd, uint32_t cmdlen, uint8_t *buf, uint32_t len)
{
	int i2cret;
	ITPI2cInfo *gsl_evt;

	gsl_evt = alloca(sizeof(*gsl_evt));
	gsl_evt->slaveAddress   = TP_I2C_DEVICE_ID;
	gsl_evt->cmdBuffer      = cmd;
	gsl_evt->cmdBufferSize  = cmdlen;
	gsl_evt->dataBuffer     = buf;
	gsl_evt->dataBufferSize = len;		
	i2cret = write(gTpInfo.tpDevFd, gsl_evt, 1);	

	return i2cret;
}

static void GSLX680_I2C_Write(uint8_t *addr, uint8_t *Wrbuf, uint32_t len)
{
    uint8_t *cmd = alloca(len+1);
    cmd[0] = *addr;
    memcpy( &cmd[1], Wrbuf, len);
    gsl_i2c_write(cmd, len+1, Wrbuf, 0);
}

