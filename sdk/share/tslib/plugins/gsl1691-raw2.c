		Wrbuf[3] = (unsigned char)((GSLX680_FW[source_line].val & 0xff000000) >> 24);
		#else
		Wrbuf[0] = (unsigned char)((GSLX680_FW[source_line].val & 0xff000000) >> 24);
		Wrbuf[1] = (unsigned char)((GSLX680_FW[source_line].val & 0x00ff0000) >> 16);
		Wrbuf[2] = (unsigned char)((GSLX680_FW[source_line].val & 0x0000ff00) >> 8);
		Wrbuf[3] = (unsigned char)(GSLX680_FW[source_line].val & 0x000000ff);
